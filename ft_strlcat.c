#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	src_len;
	size_t	dst_len;

	i = 0;
	src_len = ft_strlen(src);
	dst_len = ft_strlen(dst);
	if (size <= dst_len)
		return (src_len + size);
	while (*dst)
		dst++;
	while (*src && i < size - dst_len - 1)
	{
		*dst++ = *src++;
		i++;
	}
	*dst = '\0';
	return (src_len + dst_len);
}
